_cache: dict[str, QtGui.QIcon] = {}


def icon(name: str) -> QtGui.QIcon:
    """Return the named icon as a QIcon (cached). Unknown names yield a null
    QIcon so a missing entry degrades to a plain text button, never a crash."""
    if name in _cache:
        return _cache[name]
    b64 = _ICON_PNG_B64.get(name)
    if not b64:
        return QtGui.QIcon()
    data = QtCore.QByteArray(base64.b64decode(b64))
    pix = QtGui.QPixmap()
    pix.loadFromData(data, "PNG")
    ic = QtGui.QIcon(pix)
    _cache[name] = ic
    return ic


def github_icon() -> QtGui.QIcon:
    """Return the GitHub mark as a QIcon."""
    return icon("github")
